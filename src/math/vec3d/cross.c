#include <mfmath.h>

t_vec3d	vec3d_cross(t_vec3d a, t_vec3d b)
{
	return ((t_vec3d){.x = a.y * b.z - a.z * b.y, .y = a.z * b.x - a.x * b.z,
		.z = a.x * b.y - a.y * b.x});
}
