#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s && *s != (char)c)
		s++;
	if (*s == (char)c)
		return ((char *)s);
	if (c == 0 && *s == '\0')
		return ((char *)s);
	return (NULL);
}
