#include "libft.h"

int		ft_countwords(char *s, char c)
{
	int		i;
	int		ok;

	i = 0;
	ok = 0;
	while (*s)
	{
		if (!ok && *s != c)
		{
			ok = 1;
			i++;
		}
		else if (ok && *s == c)
			ok = 0;
		s++;
	}
	return (i);
}
